/**
 * generate-build-config.mjs
 *
 * Reads CLOUDFRONT_DOMAIN from the environment and writes
 * frontend/src/config.ts with baked-in values.  When env vars are absent the
 * committed defaults are left unchanged, so the repo always has a valid file.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const configPath = resolve(__dirname, "../src/config.ts");

const cloudFrontDomain = process.env["CLOUDFRONT_DOMAIN"] ?? "";

// Only rewrite the file when the env var is explicitly provided.
// This prevents overwriting the repo defaults with empty strings on a plain `npm run build`.
if (!process.env["CLOUDFRONT_DOMAIN"]) {
  process.exit(0);
}

const current = readFileSync(configPath, "utf-8");
const updated =
  `// Build-time configuration — regenerated by \`npm run build\` from env vars.\n` +
  `// Defaults here are safe for local proxy development (proxy routes /episodes/* to CloudFront).\n` +
  `// To bake real values in: set CLOUDFRONT_DOMAIN before running the build.\n` +
  `\n` +
  `/** CloudFront distribution origin. Empty string falls back to proxy-relative URLs in dev. */\n` +
  `export const CLOUDFRONT_DOMAIN = ${JSON.stringify(cloudFrontDomain)};\n` +
  `\n` +
  `/** Local proxy route that serves the public viewer shell against repo-managed episodes. */\n` +
  `export const LOCAL_PREVIEW_BASE_PATH = "/preview";\n` +
  `\n` +
  `/** How often the browser polls the status endpoint while a job is running (ms). */\n` +
  `export const POLL_INTERVAL_MS = 3_000;\n` +
  `\n` +
  `/** localStorage key used to persist an in-progress job ID across page reloads. */\n` +
  `export const JOB_ID_STORAGE_KEY = "linions.jobId";\n`;

if (current !== updated) {
  writeFileSync(configPath, updated, "utf-8");
  console.log("generate-build-config: wrote frontend/src/config.ts from env vars.");
}
